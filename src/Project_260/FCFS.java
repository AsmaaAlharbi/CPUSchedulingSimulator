    public FCFS() throws FileNotFoundException {
        initComponents();

        File file = new File("input.txt");
        Scanner input = new Scanner(file);

        float TotalWaitTime = 0, AverageWaitTime = 0;
        int[] Process = new int[n];
        int[] Arrival = new int[n];
        int[] Burst = new int[n];

        //for loop to read the input from the file and save them into the arrays
        for (int i = 0; i < n; i++) {
            Process[i] = input.nextInt();//Process number
            Arrival[i] = input.nextInt();//Arrival time
            Burst[i] = input.nextInt();//Burst time
        }

        // -----------------------------------------------------------------------------------------------
        // First come First served Algorithm
        // -----------------------------------------------------------------------------------------------
        // Creat Waiting time, Termination time, Turnaround time and Response time Arrays (for FCFS)
        float[] FCFSWaiting = new float[n];
        float[] FCFSTermination = new float[n];
        float[] FCFSTurnaround = new float[n];
        float[] FCFSResponse = new float[n];

        float[] B = new float[n];
        for (int i = 0; i < n; i++) {
            B[i] = (int) Burst[i];
        }

        //caculate waiting time  
        FCFSWaiting[0] = 0;
        for (int i = 1; i < n; i++) {
            FCFSWaiting[i] = B[i - 1] + FCFSWaiting[i - 1];
        }
        //calculate Termination time
        for (int i = 0; i < n; i++) {
            FCFSTermination[i] = Burst[i] + FCFSWaiting[i];
        }
        //calculate Turnaround Time
        for (int i = 0; i < n; i++) {
            FCFSTurnaround[i] = FCFSTermination[i] - Arrival[i];
        }
        //calculate Response Time
        for (int i = 0; i < n; i++) {
            FCFSResponse[i] = FCFSTurnaround[i] - B[i] - Arrival[i];
        }

        //Printing order of process execution
        FCFS += "\n___Printing order of process execution(FCFS)_________";
        FCFS += ("\n");
        for (int i = 0; i < n; i++) {
            FCFS += ("      P-" + Process[i] + "- ");
        }
        FCFS += ("\n");

        //Drawing Gantt chart representing order of process execution
        FCFS += ("\n\n___Drawing Gantt chart_____________________________");
        String GanttChart = printGanttChart(Process, Burst, FCFSTurnaround);
        FCFS += (GanttChart);

        //print processes details
        FCFS += ("\n\nPrinting detailed results of each process:");
        for (int i = 0; i < n; i++) {
            FCFS += ("\n ___Process" + Process[i] + "_________________________");
            FCFS += ("\n Termination time = " + FCFSTermination[i]);
            FCFS += ("\n Response time = " + FCFSResponse[i]);
            FCFS += ("\n Waiting time = " + FCFSWaiting[i]);
            FCFS += ("\n Turnaround time = " + FCFSTurnaround[i] + "\n");

        }
        //Calculating Average Weighting Time
        FCFS += ("\n\n___________________________________________________");
        FCFS += ("\nTotal and Average Waiting Time:");
        for (int i = 0; i < n; i++) {
            TotalWaitTime += FCFSWaiting[i];
        }
        AverageWaitTime = TotalWaitTime / n;
        FCFS += ("\nTotal Waiting Time= " + TotalWaitTime);
        FCFS += ("\nAverage Waiting Time= " + AverageWaitTime + "\n");

        Font f = new Font("Monospaced", Font.PLAIN, 12);
        text.setFont(f);
        text.setText(FCFS);

    }

