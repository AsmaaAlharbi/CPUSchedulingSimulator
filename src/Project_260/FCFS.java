    public static String printGanttChart(int[] process, int[] Burst, float[] turnaroundTimes) {

        String GanttChart = "";

        GanttChart += ("\n ");
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < Burst[i]; j++) {
                GanttChart += ("--");
            }
            GanttChart += ("  ");
        }

        GanttChart += ("\n|");

        for (int i = 0; i < n; i++) {
            for (int j = 0; j < Burst[i] - 1; j++) {
                GanttChart += (" ");
            }

            GanttChart += ("P" + process[i]);
            for (int j = 0; j < Burst[i] - 1; j++) {

                GanttChart += (" ");
            }

            GanttChart += (" |");
        }

        GanttChart += ("\n ");
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < Burst[i]; j++) {
                GanttChart += ("--");
            }
            GanttChart += ("  ");
        }
        GanttChart += ("\n");

        GanttChart += ("0");
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < Burst[i]; j++) {

                GanttChart += ("  ");
            }
            if (turnaroundTimes[i] > 9) {

                GanttChart += ("\b");
            }
            GanttChart += (int) (turnaroundTimes[i]);
        }
        GanttChart += ("\n");

        return GanttChart;
    }

